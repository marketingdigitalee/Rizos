				En el marco del proyecto Profesión Líder, El Espectador entrega anualmente el Premio de Liderazgo El Espectador, por medio del cual busca destacar a organizaciones, empresarios y emprendedores que con su actuar conciben, desarrollan y ejecutan en su día a día acciones que son verdaderas transformaciones e impactan en dimensiones de la vida de las organizaciones, comunidades y la sociedad en general mediante la construcción y el desarrollo visibles y comprobables de modelos de liderazgo que propenden por altos estándares de calidad de vida, competitividad, equidad y sostenibilidad, sobre la base de un propósito humanista para los actores del sistema empresarial y social (empleados y sus familias, clientes, accionistas, reguladores).
