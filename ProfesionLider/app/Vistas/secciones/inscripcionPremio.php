          <h4>ELEMENTOS Y CRITERIOS TRANSVERSALES A TODAS LAS CATEGORÍAS</h4>
        </li>
        <li class="a">
          <ul class="ulEstilos">
            <li>&#8226;  Se admitirá la postulación de empresas y empresarios que no tengan actividades ilegales ni antecedentes comprobados y/o acciones judiciales en curso.  </li>
            <li>&#8226; Se admitirá la postulación de todo tipo de organizaciones, tanto privadas como públicas, y de diferentes dimensiones (pequeñas, medianas, grandes).  </li>
            <li>&#8226; Los postulados podrán participar en más de una categoría. Las categorías no son excluyentes. </li>
            <li>&#8226;  Las empresas o personas que se postulen deben mostrar de manera documentada y avalada por un miembro autorizado dentro de la organización (quien deberá firmar lo presentado) el impacto medible y demostrable asociado a una meta retadora en algún componente clave del negocio o de beneficio para la sociedad. </li>
            <li>&#8226;  Las empresas postuladas deben contar con estatutos y políticas anticorrupción y sistemas de riesgo apoyados desde las más altas instancias de gobierno de la organización, que rechacen y eviten actos de corrupción e ilegalidad a su alrededor y ejecuten medidas legales ejemplares para quienes incurren en faltas en este sentido.</li>
            <li>&#8226; Los postulados deben demostrar que han implementado estrategias claras y contundentes y modelos de negocio, de gestión y de desarrollo de capital humano altamente innovador, que beneficien la calidad de vida de todos los actores del negocio (empleados, familias, accionistas, clientes y entorno) y contribuyan a la generación de empleos y nuevas oportunidades para el negocio. </li>
            <li>&#8226;  Los postulados deben mostrar un enfoque hacia programas claros y robustos de gobierno corporativo que propendan por la sostenibilidad, el compromiso con asuntos como el medio ambiente, la equidad social, la equidad de género, la innovación (social) y el cuidado del cliente.</li>
