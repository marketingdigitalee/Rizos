          	<h4>CATEGORÍAS</h4>
        </li>
        <li class="a">
        <p>
        	<ul class="ulEstilos">
        		<li>
        		<strong>I. LIDERAZGO SOSTENIBLE EN ACCIÓN</strong></br>
				Reconocimiento a la persona o grupo de personas organizadas en una estructura, forma u organización y esté(n) en pos de una gran meta que implique procesos de transformación importantes y significativos cuya duración sea de al menos 10 años, con iniciativas y acciones visibles de corto, mediano y largo plazo y que además estén creando valor en su entorno. El (los) participante(s) debe(n) mostrar un planteamiento claro, conciso, retador, que refleje el carácter distintivo del negocio, grupo u organización, con énfasis en acciones de tipo ambiental, humanitario, de sostenibilidad y desarrollo cultural. 

				</li>
        		<li>
        		<strong>II. ÉTICA Y GOBIERNO CORPORATIVO </strong></br>
				A la organización que establezca claramente un programa comprobado de gobierno corporativo que integre un marco de políticas que, apoyado en lo legal, construya e implemente acciones claras que reflejen la transparencia y probidad referida a todos los campos de la gestión del negocio, la cultura y los actores de la compañía, en posibles ambientes adversos en los cuales puedan existir presiones que impliquen riesgo a la probidad, los valores corporativos, la responsabilidad social, la sostenibilidad y la viabilidad de la empresa.

 
				La organización debe contar con un proyecto o programa de impacto que tenga integrado el marco del modelo o las políticas de gobierno corporativo, legal y ético establecido en su plataforma estratégica, así como la dirección correcta de actuación del negocio, soportados en políticas de transparencia y anticorrupción.

				</li>

        		<li>
        		<strong>III. LÍDER REVELACIÓN </strong></br>
				El líder, empleado, emprendedor, independiente, movilizador o activista social menor de treinta años que se destaca por su carácter altamente innovador, el cual se ve reflejado de manera constante en proyectos viables y acciones para mejorar la calidad de vida de los actores del negocio. Debe ser una persona que se caracteriza por promover acciones que se traduzcan en cambios importantes de la compañía. 
                </br>
				Debe contar con pensamiento creativo, ser innovador, tener capacidad de transformación digital y de apropiación de tecnologías al servicio de proyectos que promueven la generación de empleo, y movilizar grupos de personas con impacto social y económico. Adicionalmente debe demostrar acciones y resultados basados en procesos que velen por la gestión del conocimiento, la inclusión, el desarrollo social, la calidad de vida de los actores de su negocio, el emprendimiento o la empresa (nuevos mercados, nuevos productos, generación de empleo, movilización social, emprendimiento y empresarismo interno y externo).

				</li>
        		<li>
				<strong>IV. LÍDER CONSCIENTE DESDE LA ESTRATEGIA Y LA CULTURA– Estrategia y Cultura “Aguas del mismo río” </strong></br>
				
				Es la persona u organización que muestra que se ha empeñado durante al menos tres años en construir un esquema práctico trabajando de manera sinérgica la estrategia basada en transformación y propósito y la construcción o implementación de un modelo de cultura como mecanismo de transformación del negocio hacia la sostenibilidad, el crecimiento y el claro impacto en índices de calidad de vida de sus colaboradores, actores implicados en el negocio y la organización, más allá de los programas convencionales de bienestar. 
                </br>   
                Por otra parte, debe mostrar que ha construido programas sólidos que en el tiempo se hayan convertido en pilares de cultura organizacional y haber desarrollado habilidades de liderazgo dentro de la organización que hayan llevado a tener un impacto positivo en la productividad del negocio, siempre orientado hacia el cumplimiento de una estrategia sólida y distintiva. También, demostrar que dichos programas han llevado a mejoras sustanciales en el interior de la compañía y en la calidad de vida de amplias comunidades del país o fuera de él.
 
        		</li>
        	</ul>

