                </br>
                Personas y organizaciones que laboran desde su propósito dan ejemplo de valores éticos, responsabilidad con la sociedad, honradez y rectitud; trabajan con gusto, disciplina, compromiso y confianza; desarrollan a otros seres humanos, e inspiran cambios notables mediante la ética, la transparencia y el buen gobierno. 
